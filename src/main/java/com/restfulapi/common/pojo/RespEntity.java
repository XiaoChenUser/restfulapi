package com.restfulapi.common.pojo;

public class RespEntity {
    private int code;
    private String msg;
    private Object data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }

    public RespEntity(RespCode respCode) {
        this.code = respCode.getCode();
        this.msg = respCode.getMsg();
    }

    public RespEntity(RespCode respCode, Object data) {
        this(respCode);
        this.data = data;
    }

    public static RespEntity success(Object data) {
        return new RespEntity(RespCode.SUCCESS,data);
    }

    public static RespEntity fail(Object data) {
        return new RespEntity(RespCode.WARN, data);
    }
}
